def get_or_add_style(document: Document, name: str, base: str, style_type=WD_STYLE_TYPE.PARAGRAPH):
    styles = document.styles
    try:
        style = styles[name]
    except KeyError:
        style = styles.add_style(name, style_type)
        style.base_style = styles[base]
    return style


