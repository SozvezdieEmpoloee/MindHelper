def insert_paragraph_after(paragraph: Paragraph, text: str = "", style: str | None = None) -> Paragraph:
    new_p = OxmlElement("w:p")
    paragraph._p.addnext(new_p)
    new_paragraph = Paragraph(new_p, paragraph._parent)
    if style:
        new_paragraph.style = style
    if text:
        new_paragraph.add_run(text)
    return new_paragraph


def insert_block_after_heading(document: Document, heading_text: str, paragraphs: list[str]) -> None:
    anchor = None
    for paragraph in document.paragraphs:
        if paragraph.text.strip() == heading_text:
            anchor = paragraph
            break
    if anchor is None:
        return
    for text in reversed(paragraphs):
        pass
    current = anchor
    for text in paragraphs:
        style = STYLE_BODY
        if text.startswith("- "):
            text = text[2:]
            style = STYLE_LIST
        current = insert_paragraph_after(current, text, style=style)


def replace_paragraph_text(document: Document, old: str, new: str) -> None:
    for paragraph in document.paragraphs:
        if paragraph.text.strip() == old:
            paragraph.clear()
            paragraph.add_run(new)


