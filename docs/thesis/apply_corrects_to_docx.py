def style_tables(document: Document) -> None:
    for table in document.tables:
        try:
            table.style = "Table Grid"
        except Exception:
            pass
        for row_idx, row in enumerate(table.rows):
            for cell in row.cells:
                cell.vertical_alignment = WD_ALIGN_VERTICAL.CENTER
                for paragraph in cell.paragraphs:
                    paragraph.style = STYLE_BODY
                    paragraph.paragraph_format.first_line_indent = Cm(0)
                    paragraph.paragraph_format.line_spacing = 1.0
                    for run in paragraph.runs:
                        run.font.name = "Times New Roman"
                        run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
                        run.font.size = Pt(10 if len(table.columns) >= 4 else 11)
                        if row_idx == 0:
                            run.bold = True


def apply_paragraph_styles(document: Document) -> None:
    source_list = False
    for paragraph in document.paragraphs:
        text = paragraph.text.strip()
        if not text:
            continue
        if text == "Список использованных источников":
            source_list = True
            paragraph.style = STYLE_INTRO
        elif text.startswith("Приложение "):
            paragraph.style = STYLE_CHAPTER
        elif paragraph.style.name.startswith("Heading 1"):
            paragraph.style = STYLE_INTRO if text in {"Реферат", "Содержание", "Определения, обозначения и сокращения", "Введение", "Заключение"} else STYLE_CHAPTER
        elif paragraph.style.name.startswith("Heading 2"):
            paragraph.style = STYLE_SECTION
        elif text.startswith("Рисунок ") or text.startswith("Таблица "):
            paragraph.style = STYLE_CAPTION
        elif source_list and paragraph.style.name.startswith("List"):
            paragraph.style = STYLE_SOURCE
        elif paragraph.style.name.startswith("List"):
            paragraph.style = STYLE_LIST
        elif paragraph.style.name != STYLE_CODE:
            paragraph.style = STYLE_BODY

        for run in paragraph.runs:
            if paragraph.style.name == STYLE_CODE:
                run.font.name = "Courier New"
                run._element.rPr.rFonts.set(qn("w:eastAsia"), "Courier New")
                run.font.size = Pt(9)
            else:
                run.font.name = "Times New Roman"
                run._element.rPr.rFonts.set(qn("w:eastAsia"), "Times New Roman")
                run.font.size = Pt(14)


def resize_images(document: Document) -> None:
    max_width = Cm(16.0)
    for idx, shape in enumerate(document.inline_shapes):
        if idx == 0:
            shape.width = Cm(16.0)
        elif shape.width > max_width:
            ratio = max_width / shape.width
            shape.width = max_width
            shape.height = int(shape.height * ratio)


