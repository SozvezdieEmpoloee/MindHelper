def clear_appendices(document: Document) -> None:
    start = None
    for paragraph in document.paragraphs:
        if paragraph.text.strip() == "Приложение А":
            start = paragraph._p
            break
    if start is None:
        return
    body = document._body._element
    remove = False
    for child in list(body):
        if child is start:
            remove = True
        if remove:
            body.remove(child)


